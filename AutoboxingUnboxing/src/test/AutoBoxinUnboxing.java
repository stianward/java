package test;
public class AutoBoxinUnboxing {
    public static void main(String[] args) {
       
        //autoboxing
        /*envolver valor prmitivo a tipo object*/
        Integer num1=1;
        System.out.println("num1 = " + num1.toString());
        
        //unboxing
        /* extraer el valor del objeto y lo convierte a tipo primtiv**/
        
        int num=num1;
        System.out.println("num = " + num);
        
    }
    
}
